<h5>Shara Hughes</h5>
<p>
    Noted for her kaleidoscopic abstractions of floating moons, knobbly trees and effulgent sunlight, Shara Hughes's brushstrokes freewheel across the canvas in a smorgasbord of colour.
    The New York-based artist, who earned a BFA from the Rhode Island School of Design in 2004 and attended the Skowhegan School of Painting and Sculpture in 2011, has a bold signature that riffs on dramatic tones and dreamlike manifestations.
    Hughes's oeuvre nods in equal spirit to the deeply saturated palettes of the early Impressionists and the darker psychological imagery of more recent artists like Forrest Bess, Philip Guston and Dana Schutz. In the manner of David Hockney and Charles Burchfield, she upends conventional rules of space and light, pulling the observer down a sensorial rabbit hole of texture, pattern, and perspective. 
    In 2017, she was announced as a participating artist in the Whitney Biennial. Hughes currently has works on exhibit at the Metropolitan Museum of Art in New York, the Denver Art Museum, and the Smithsonian American Art Museum in Washington DC.
    She will be stopping by on December 6th to give talks about her inspiration and show off her new up and coming work, be sure to get your tickets to this event</p>

<h5>
Hilary Pecis</h5>
<p>
  Hilary Pecis’s still life and landscape paintings inspire a vibrant storybook quality, incarnating domestic vignettes and weekend markets in characterful frames, bright colours and Fauvist flourishes.
  Featuring recognisable objects, her art breathes an air à la Matisse, romanticising the quotidian and enveloping the observer in a familiar aura. 
  The California native, who holds a BFA and MFA from the California College of the Arts, San Francisco, has hosted solo exhibitions at such prestigious venues as Rockefeller Center, New York; Timothy Taylor Gallery, London; Spurs Gallery, Beijing; The Pit, Los Angeles; and Guerrero Gallery, San Francisco.
  She will be showing off her work on November 9th at our gallery so be sure to get your tickets. </p>
